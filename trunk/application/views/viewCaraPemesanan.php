<h3>Cara Pemesanan</h3>

1. Setelah Melakukan konfirmasi pemesanan, hubungi/kirim nomor pemesanan anda (SMS) ke nomor berikut 085722133087									
									
<br>2. Kami akan mengkonfirmasi kembali pesanan anda dan menjelaskan detail dari nomor rekening untuk pembayaran									
									
<br>3. Lakukan pembayaran melalui Transfer ATM									
									
<br>4. Lakukan konfirmasi Pembayaran melalui form pembayaran									
									
<br>5. Setelah dilakukan konfirmasi ,Pesanan segera kami kirim melalui JNE dan sampai alamat dalam waktu sesuai ketentuan 									
      paket pengiriman dan lokasi									


